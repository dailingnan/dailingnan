package com.eleme.menumanage.pojo;

public class StyleFood {
	private int sid;
	private String sname;
	public StyleFood(){}
	public StyleFood(int sid, String sname) {
		this.sname = sname;
	}
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	
}
